package com.buyopicadmin.admin.models;

public class ConsumerInformation {
	
	public String mConsumerEmailId;
	public String mConsumerId;
	public String mConsumerName;
	public String mConsumerPhoneNum;
	
	public String getmConsumerName() {
		return mConsumerName;
	}
	public void setmConsumerName(String mConsumerName) {
		this.mConsumerName = mConsumerName;
	}
	public String getmConsumerPhoneNum() {
		return mConsumerPhoneNum;
	}
	public void setmConsumerPhoneNum(String mConsumerPhoneNum) {
		this.mConsumerPhoneNum = mConsumerPhoneNum;
	}
	public String getmConsumerEmailId() {
		return mConsumerEmailId;
	}
	public void setmConsumerEmailId(String mConsumerEmailId) {
		this.mConsumerEmailId = mConsumerEmailId;
	}
	public String getmConsumerId() {
		return mConsumerId;
	}
	public void setmConsumerId(String mConsumerId) {
		this.mConsumerId = mConsumerId;
	}
	

}
