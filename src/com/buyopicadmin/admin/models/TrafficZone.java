package com.buyopicadmin.admin.models;

public class TrafficZone {

	private String mZoneName;
	private String mTotalCustomers;
	private String mTotalTimeInZone;
	private String mAvgTimeinZone;

	public String getmZoneName() {
		return mZoneName;
	}

	public void setmZoneName(String mZoneName) {
		this.mZoneName = mZoneName;
	}

	public String getmTotalCustomers() {
		return mTotalCustomers;
	}

	public void setmTotalCustomers(String mTotalCustomers) {
		this.mTotalCustomers = mTotalCustomers;
	}

	public String getmTotalTimeInZone() {
		return mTotalTimeInZone;
	}

	public void setmTotalTimeInZone(String mTotalTimeInZone) {
		this.mTotalTimeInZone = mTotalTimeInZone;
	}

	public String getmAvgTimeinZone() {
		return mAvgTimeinZone;
	}

	public void setmAvgTimeinZone(String mAvgTimeinZone) {
		this.mAvgTimeinZone = mAvgTimeinZone;
	}

}
